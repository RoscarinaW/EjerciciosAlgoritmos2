#Calculadora de promedio de 5 numeros
print("Calculadora de promedio de 5 numeros")
a = float(input("Ingrese el primer numero "))
b = float(input("Ingrese el segundo numero "))
c = float(input("Ingrese el tercero numero "))
d = float(input("Ingrese el cuarto numero "))
e = float(input("Ingrese el quinto numero "))

promedio = (a + b + c + d + e) / 5

print(f"/n El promedio total de {a}, {b}, {c}, {d} y {e} es: {promedio} ")