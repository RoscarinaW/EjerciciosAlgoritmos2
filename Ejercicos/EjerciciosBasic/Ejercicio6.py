#Calculadora de porcentajes 30% 60% y 90%
print("Calculadora de Porcentaje 30%, 60% y 90%")

A = float(input("Ingrese el valor a calcular "))

porcentaje_30 = A * 0.30
porcentaje_60 = A * 0.60
porcentaje_90 = A * 0.90

print(f"\nEl resultado de los porcentajes de el numero {A}")
print(f" El resultado de 30% es: {porcentaje_30}")
print(f" El resultado de 60% es: {porcentaje_60}")
print(f" El resultado de 90% es: {porcentaje_90}")